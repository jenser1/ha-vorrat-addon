def get_translation(lang="de"):
    """Gibt Übersetzungs-Dict für die gewählte Sprache zurück."""
    return TRANSLATIONS.get(lang, TRANSLATIONS["de"])

def format_currency(amount, currency="EUR"):
    """Formatiert einen Betrag mit Währungssymbol."""
    if amount is None:
        return ""
    c = CURRENCIES.get(currency, CURRENCIES["EUR"])
    formatted = f"{amount:,.2f}".replace(",", "X").replace(".", ",").replace("X", ".")
    if c["position"] == "before":
        return f"{c['symbol']} {formatted}"
    else:
        return f"{formatted} {c['symbol']}"
